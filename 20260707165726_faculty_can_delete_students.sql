/*
# QR Attendance System — Full Schema

## Summary
Creates all tables required for the QR-based attendance system with three user roles:
Admin, Faculty, and Student. Each user authenticates via Supabase Auth and has a
corresponding profile. Faculty can generate time-limited QR sessions; students scan
them to mark attendance. Admins manage all data.

## New Tables

### profiles
Extends auth.users with display name, role, photo URL, and contact info.
- id (uuid, FK → auth.users, PK)
- full_name (text)
- role (text: 'admin' | 'faculty' | 'student')
- photo_url (text, nullable)
- phone (text, nullable)
- created_at (timestamptz)

### students
Student-specific data linked to profiles.
- id (uuid, PK, FK → profiles)
- reg_no (text, unique)
- course (text)
- section (text)
- semester (int)
- department (text)

### faculty
Faculty-specific data linked to profiles.
- id (uuid, PK, FK → profiles)
- employee_id (text, unique)
- department (text)
- designation (text)

### subjects
Subjects taught by faculty members.
- id (uuid, PK)
- subject_code (text, unique)
- subject_name (text)
- faculty_id (uuid, FK → faculty)
- section (text)
- semester (int)
- department (text)
- created_at (timestamptz)

### qr_sessions
Time-limited QR sessions generated by faculty for attendance.
- id (uuid, PK)
- subject_id (uuid, FK → subjects)
- faculty_id (uuid, FK → profiles)
- qr_token (text, unique) — the encoded value inside the QR
- generated_at (timestamptz)
- expires_at (timestamptz)
- is_active (boolean)

### attendance_records
Individual attendance entries per student per QR session.
- id (uuid, PK)
- session_id (uuid, FK → qr_sessions)
- student_id (uuid, FK → profiles)
- marked_at (timestamptz)
- status (text: 'present' | 'absent' | 'leave')

## Security
- RLS enabled on all tables.
- Profiles: users read/update own; admin reads all.
- Students/Faculty: owner can read/update own; admin full control.
- Subjects: faculty can CRUD own; all authenticated can read; admin full control.
- QR sessions: faculty creates own; all authenticated can read active sessions.
- Attendance: students insert own (once per session); faculty/admin can read all.
*/

-- ──────────────────────────────────────────
-- profiles
-- ──────────────────────────────────────────
CREATE TABLE IF NOT EXISTS profiles (
  id          uuid PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  full_name   text NOT NULL DEFAULT '',
  role        text NOT NULL DEFAULT 'student' CHECK (role IN ('admin','faculty','student')),
  photo_url   text,
  phone       text,
  created_at  timestamptz NOT NULL DEFAULT now()
);

ALTER TABLE profiles ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "profiles_select_own" ON profiles;
CREATE POLICY "profiles_select_own" ON profiles FOR SELECT
  TO authenticated USING (auth.uid() = id);

DROP POLICY IF EXISTS "profiles_select_admin" ON profiles;
CREATE POLICY "profiles_select_admin" ON profiles FOR SELECT
  TO authenticated USING (
    EXISTS (SELECT 1 FROM profiles p WHERE p.id = auth.uid() AND p.role = 'admin')
  );

DROP POLICY IF EXISTS "profiles_insert_own" ON profiles;
CREATE POLICY "profiles_insert_own" ON profiles FOR INSERT
  TO authenticated WITH CHECK (auth.uid() = id);

DROP POLICY IF EXISTS "profiles_update_own" ON profiles;
CREATE POLICY "profiles_update_own" ON profiles FOR UPDATE
  TO authenticated USING (auth.uid() = id) WITH CHECK (auth.uid() = id);

DROP POLICY IF EXISTS "profiles_update_admin" ON profiles;
CREATE POLICY "profiles_update_admin" ON profiles FOR UPDATE
  TO authenticated
  USING (EXISTS (SELECT 1 FROM profiles p WHERE p.id = auth.uid() AND p.role = 'admin'))
  WITH CHECK (EXISTS (SELECT 1 FROM profiles p WHERE p.id = auth.uid() AND p.role = 'admin'));

DROP POLICY IF EXISTS "profiles_delete_admin" ON profiles;
CREATE POLICY "profiles_delete_admin" ON profiles FOR DELETE
  TO authenticated USING (
    EXISTS (SELECT 1 FROM profiles p WHERE p.id = auth.uid() AND p.role = 'admin')
  );

