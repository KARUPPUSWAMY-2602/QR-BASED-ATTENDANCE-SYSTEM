CREATE POLICY "qr_sessions_update_faculty" ON qr_sessions FOR UPDATE
  TO authenticated
  USING (auth.uid() = faculty_id OR EXISTS (SELECT 1 FROM profiles p WHERE p.id = auth.uid() AND p.role = 'admin'))
  WITH CHECK (auth.uid() = faculty_id OR EXISTS (SELECT 1 FROM profiles p WHERE p.id = auth.uid() AND p.role = 'admin'));

