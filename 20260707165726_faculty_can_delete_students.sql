CREATE POLICY "qr_sessions_insert_faculty" ON qr_sessions FOR INSERT
  TO authenticated WITH CHECK (
    auth.uid() = faculty_id AND
    EXISTS (SELECT 1 FROM profiles p WHERE p.id = auth.uid() AND p.role IN ('admin','faculty'))
  );

